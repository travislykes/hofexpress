    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $page_title = "User Type Manager";
        $usertypes = UserType::all();
       
        return view('admin.settings.usertype', compact('page_title','usertypes'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $usertype = new UserType;

        $usertype->name = $request->name;
        $usertype->description = $request->description;

        $usertype->save();
        session()->flash('message', 'User Type Created successfully!');
        return redirect()->route('admin.user.types');
    }

